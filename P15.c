//  Objective : Mathematical Operations On Character to get Character

#include<stdio.h>
int main()
{
    int a = 3250;
    char c = 'A';
    printf("Name : Aryan Gupta\n Class : 1A\n Program : BCA\n");

    printf("Number after Addition of int and char  is %c\n", a+c);

    printf("Number after Substraction of int and char  is %c\n", a-c);

    printf("Number after Multiplication of int and char  is %c\n", a*c);  

    printf("Number after Division of int and char  is %c\n", a/c);


    return 0;
}